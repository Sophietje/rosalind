def encode(rna_string):
    i=0
    result = ""
    while (i+2 < len(rna_string)):
        c1 = rna_string[i]
        c2 = rna_string[i+1]
        c3 = rna_string[i+2]
        key = c1+c2+c3
        encoding = RNA_table[key]
        if encoding == "Stop":
            return result
        else:
            result += encoding
        i += 3
    return result


